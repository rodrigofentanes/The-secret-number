// Os colchetes são utilizados para representar um Array/Lista.
var arrayList = ['Maçã', 'Banana'];

// Forma de enxergar o valor na primeira posição da lista.
console.log(arrayList[0]);

// Forma de percorrer a lista automaticamente.
for (let index = 0; index < arrayList.length; index++) {
  console.log(arrayList[index]);
}

